    private void jLabelClientes1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelClientes1MouseClicked
        // TODO add your handling code here:
        jTabbedPanePrincipal.setSelectedComponent(jPanelCliente);
         try {
            conn.iniciarConexao();
            ArrayList<Cliente> lista;
            lista = conn.selectCli();
            if(lista.isEmpty()){
                JOptionPane.showMessageDialog(null, "não há itens cadastrados");
            }
            else{
                int linha=0;

                for(Cliente cliente: lista){
                    jTableListaCli.setValueAt(cliente.getIdCli(), linha, 0);
                    jTableListaCli.setValueAt(cliente.getNomeCli(), linha, 1);
                    jTableListaCli.setValueAt(cliente.getCnpj(), linha, 2);
                    jTableListaCli.setValueAt(cliente.getEnderecoCli(), linha, 3);
                    jTableListaCli.setValueAt(cliente.getTelefoneCli(), linha, 4);
                    jTableListaCli.setValueAt(cliente.getCelularCli(), linha, 5);
                    jTableListaCli.setValueAt(cliente.getEmailCli(), linha, 6);
                    linha++;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        conn.fecharConexao();
        
    }//GEN-LAST:event_jLabelClientes1MouseClicked

