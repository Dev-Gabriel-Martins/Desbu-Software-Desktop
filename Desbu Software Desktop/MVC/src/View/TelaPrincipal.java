    public void atualizarCli(){
    try {
            conn.iniciarConexao();
            ArrayList<Cliente> lista;
            lista = conn.selectCli();
            if(lista.isEmpty()){
                JOptionPane.showMessageDialog(null, "não há itens cadastrados");
            }
            else{
                int linha=0;

                for(Cliente cliente: lista){
                    jTableListaCli1.setValueAt(cliente.getIdCli(), linha, 0);
                    jTableListaCli1.setValueAt(cliente.getNomeCli(), linha, 1);
                    jTableListaCli1.setValueAt(cliente.getCnpj(), linha, 2);
                    jTableListaCli1.setValueAt(cliente.getEnderecoCli(), linha, 3);
                    jTableListaCli1.setValueAt(cliente.getTelefoneCli(), linha, 4);
                    jTableListaCli1.setValueAt(cliente.getCelularCli(), linha, 5);
                    jTableListaCli1.setValueAt(cliente.getEmailCli(), linha, 6);
                    linha++;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        conn.fecharConexao();
    }
    public void AtualizarAbertos(){
    
    try {
            conn.iniciarConexao();
            ArrayList<Chamado> lista2;
            lista2 = conn.selectChamAberto();
            if(lista2.isEmpty()){
                JOptionPane.showMessageDialog(null, "Não a Chamados abertos");
                
            }
            else{
                int linha=0;

                for(Chamado chamado: lista2){
                    jTableListaChamAbertos.setValueAt(chamado.getIdCham(), linha, 0);
                    jTableListaChamAbertos.setValueAt(chamado.getDescAberturaCham(), linha, 1);
                    jTableListaChamAbertos.setValueAt(chamado.getPrioridadeCham(), linha, 2);
                    jTableListaChamAbertos.setValueAt(chamado.getIdCli(), linha, 3);                 
                    linha++;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        conn.fecharConexao();
        
    }
    
