    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed
        // TODO add your handling code here:
        
        
            
        TelaPrincipal principal = new TelaPrincipal();
        Analista analista = new Analista();
        String nomeUsu = jTextFieldUsuario.getText();
        String senhaUsu = jPasswordField.getText();
                
        analista.setNomeUsu(nomeUsu);
        analista.setSenhaUsu(senhaUsu);
  
        try {
            conn.iniciarConexao();
            rs = conn.autenticarUsuario(analista);
            
            if(rs.next()){
                
                
                principal.setVisible(true);
                this.dispose();
                
            }else{
                jLabelInvalido.setVisible(true);
                jTextFieldUsuario.setText("");
                jPasswordField.setText("");
            }
            Boolean acesso  = conn.acessoUsuario(analista);
            if(acesso){
            principal.jLabelAdm.setVisible(true);
            
            }else{
            principal.jLabelAdm.setVisible(false);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        conn.fecharConexao();
        
    
        
        
        
    }//GEN-LAST:event_jButtonEntrarActionPerformed

